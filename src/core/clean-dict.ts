export function applyCleanOverrides(target: Language, translations: Map<string, string>): void {
  for (const [pt, overrides] of Object.entries(CLEAN_OVERRIDES)) {
    const corrected = overrides[target];
    if (corrected && translations.has(pt)) translations.set(pt, corrected);
  }
}

export function lookupPhrase(text: string, target: Language): string | null {
  const lower = text.toLowerCase().trim();
  const phrase = PHRASE_OVERRIDES[lower];
  if (phrase && phrase[target]) return phrase[target]!;
  return null;
}
